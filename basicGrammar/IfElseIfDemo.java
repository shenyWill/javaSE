public class IfElseIfDemo{
    public static void main(String[] args) {
        int i = 66;
        if (i > 60) {
            System.out.println("i > 60");
        }else if(i > 80) {
            System.out.println("i > 80");
        } else {
            System.out.println("i < 60");
        }
    }
}