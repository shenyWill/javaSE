package cn.yuanwill.exceptions;

public class FUException extends Exception {

	public FUException() {
		super();
		
	}

	public FUException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public FUException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public FUException(String arg0) {
		super(arg0);
		
	}

	public FUException(Throwable arg0) {
		super(arg0);
		
	}

	

}
